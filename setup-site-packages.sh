#! /bin/bash

# make directory
mkdir -p site-packages

# bootstrap
curl -f -X GET https://stackpath.bootstrapcdn.com/bootstrap/4.3.1/css/bootstrap.min.css > ./site-packages/bootstrap.min.css
curl -f -X GET https://stackpath.bootstrapcdn.com/bootstrap/4.3.1/js/bootstrap.min.js > ./site-packages/bootstrap.min.js

# jquery
curl -f -X GET https://code.jquery.com/jquery-3.5.1.min.js > ./site-packages/jquery-3.5.1.min.js

# jquery raty
curl -f -X GET https://raw.githubusercontent.com/wbotelhos/raty/master/lib/jquery.raty.js > ./site-packages/jquery.raty.js